// Preprocessor directive to include files in our program
#include <iostream>

// This is a comment!

// Valid C++ programs MUST HAVE main(){}
int main(){
    // cout comes from <iostream> that allows us to print output to the screen
    // std::cout prints the string, and it must be followed by <<
    // ";" is used to end a statement, bit like JS
    std::cout << "Hello World!";
    // This is the "Exit Status" out of the program
    return 0;
}